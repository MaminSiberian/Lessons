using UnityEngine;

public class GameManager : MonoBehaviour
{
    public static GameManager instance { get; private set; }

    private void Awake()
    {
        if (instance == null)
        {
            instance = this;
            DontDestroyOnLoad(this.gameObject);
            return;
        }

        Destroy(gameObject);
    }

    public void Debug()
    {
        UnityEngine.Debug.Log("GameManager singleton");
    }
}
